#include <unistd.h>

static void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void		ft_putnbr(int n)
{
	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			n = -n;
			ft_putchar('-');
		}
		if (n / 10)
			ft_putnbr(n / 10);
		ft_putchar(n % 10 + 48);
	}
}
